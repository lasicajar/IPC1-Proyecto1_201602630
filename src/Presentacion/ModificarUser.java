    private void jtbbuscarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jtbbuscarMouseClicked
        boolean encontrar = false;
        if (jtxid.getText().length() == 0) {
            JOptionPane.showMessageDialog(null, "Ingrese ID valido...", "Faltan Datos ", JOptionPane.WARNING_MESSAGE);
        } else {
            for (int i = 0; i < biblioteca.Biblioteca.contuser; i++) {
                if (biblioteca.Biblioteca.users[i].getId().equals(Integer.parseInt(jtxid.getText()))) {
                    jtxnombre.setText(biblioteca.Biblioteca.users[i].getNombre());
                    jtxapellido.setText(biblioteca.Biblioteca.users[i].getApellido());
                    jtxuser.setText(biblioteca.Biblioteca.users[i].getUser());
                    encontrar = true;
                }

            }
            if (!encontrar) {
                JOptionPane.showMessageDialog(null, "El ID no esta registrado", "Sin Registro ", JOptionPane.WARNING_MESSAGE);
            }

        }

        // TODO add your handling code here:
    }//GEN-LAST:event_jtbbuscarMouseClicked

    private void jbtmodiuserMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jbtmodiuserMouseClicked
        for (int i = 0; i < biblioteca.Biblioteca.contuser; i++) {
            if (biblioteca.Biblioteca.users[i].getId().equals(Integer.parseInt(jtxid.getText()))) {
                biblioteca.Biblioteca.users[i].setNombre(jtxnombre.getText());
                biblioteca.Biblioteca.users[i].setApellido(jtxapellido.getText());
                biblioteca.Biblioteca.users[i].setUser(jtxuser.getText());
                biblioteca.Biblioteca.users[i].setRol((String) jcbrol.getSelectedItem());
                biblioteca.Biblioteca.users[i].setPassword(jtxpass.getText());
                
                JOptionPane.showMessageDialog(null, "El usuario fue modificado exitosamente", "Usuario Modificado", JOptionPane.INFORMATION_MESSAGE);
               setVisible(false);

            }

        } // TODO add your handling code here:
    }//GEN-LAST:event_jbtmodiuserMouseClicked

